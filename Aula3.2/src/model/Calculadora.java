package model;

public class Calculadora {

	public int calcAdicao(int primeiro, int segundo) {
		int adicao = primeiro + segundo;
		return adicao;
	}
	
	public int calcSubtracao(int primeiro, int segundo) {
		int subtracao = primeiro - segundo;
		return subtracao;
	}
	
	public int calcMultiplicacao(int primeiro, int segundo) {
		int multiplicacao = primeiro * segundo;
		return multiplicacao;
	}
	
	public int calcDivisao(int primeiro, int segundo) {
		int divisao = primeiro - segundo;
		return divisao;
	}
}
